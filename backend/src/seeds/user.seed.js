const seedDatabase = async () => {
  try {
    await connectDB();

    // 🔐 Use env password or fallback
    const DEFAULT_SEED_PASSWORD =
      process.env.SEED_PASSWORD || "Temp@123";

    const hashedPassword = await bcrypt.hash(
      DEFAULT_SEED_PASSWORD,
      12
    );

    // ✅ Attach hashed password to all users
    const usersWithHashedPasswords = seedUsers.map((user) => ({
      ...user,
      password: hashedPassword,
    }));

    await User.insertMany(usersWithHashedPasswords);

    logger.info("Database seeded successfully", {
      context: "seed.users",
    });
  } catch (error) {
    logger.error("Error seeding database", {
      context: "seed.users",
      error: error.message,
      stack: error.stack,
    });
  }
};

// Call the function
seedDatabase();